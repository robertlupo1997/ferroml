def print_deployment(result: dict) -> None:
    """Print human-readable deployment summary."""
    print(f"\n{'='*60}")
    print(f"DEPLOYMENT PREPARED  |  {result['model_name']}")
    print(f"{'='*60}")
    print(f"\nFeatures: {result['n_features']}")
    print(f"Files created: {len(result['files_created'])}")
    for f in result["files_created"]:
        print(f"  - {f}")
    print(f"\nModel sizes:")
    for fmt, size in result["model_size_bytes"].items():
        if size > 0:
            print(f"  {fmt}: {size:,} bytes")
    print(f"\nTo start the API:")
    print(f"  cd {os.path.dirname(result['api_template'])}")
    print(f"  uvicorn app:app --host 0.0.0.0 --port 8000")
    print()
