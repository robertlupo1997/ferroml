"""Export a trained FerroML model for production deployment.

Usage: Claude runs this after training to generate deployment artifacts.
Output: Model files, inference script, FastAPI endpoint, Dockerfile.
"""
from __future__ import annotations

import json
import os
from pathlib import Path


def prepare_deployment(
    model: object,
    model_name: str,
    feature_names: list[str],
    scaler: object | None = None,
    output_dir: str = "./deploy",
) -> dict:
    """Export model and generate deployment templates.

    Parameters
    ----------
    model : object
        Fitted FerroML model.
    model_name : str
        Human-readable name (e.g. "gradient_boosting_v2").
    feature_names : list[str]
        Input feature names in order.
    scaler : object or None
        Optional fitted FerroML scaler to include.
    output_dir : str
        Directory for deployment artifacts.

    Returns
    -------
    dict with files_created, model_size_bytes, inference_template, api_template
    """
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    files_created: list[str] = []
    model_sizes: dict[str, int] = {}

    # --- Save model in multiple formats ---
    for fmt, ext, save_fn in [
        ("json", ".json", "save_json"),
        ("msgpack", ".msgpack", "save_msgpack"),
        ("bincode", ".bin", "save_bincode"),
    ]:
        path = str(out / f"model{ext}")
        try:
            getattr(model, save_fn)(path)
            size = os.path.getsize(path)
            model_sizes[fmt] = size
            files_created.append(path)
        except Exception:
            model_sizes[fmt] = -1  # format not supported

    # Save scaler if provided
    if scaler is not None:
        scaler_path = str(out / "scaler.json")
        try:
            scaler.save_json(scaler_path)
            files_created.append(scaler_path)
        except Exception:
            pass

    # Save feature names
    meta_path = str(out / "metadata.json")
    metadata = {
        "model_name": model_name,
        "feature_names": feature_names,
        "n_features": len(feature_names),
        "has_scaler": scaler is not None,
        "formats": {k: v for k, v in model_sizes.items() if v > 0},
    }
    with open(meta_path, "w") as f:
        json.dump(metadata, f, indent=2)
    files_created.append(meta_path)

    # --- Generate inference.py ---
    model_class = type(model).__name__
    scaler_class = type(scaler).__name__ if scaler else None

    inference_template = _generate_inference(model_class, scaler_class, feature_names)
    inference_path = str(out / "inference.py")
    with open(inference_path, "w") as f:
        f.write(inference_template)
    files_created.append(inference_path)

    # --- Generate FastAPI app ---
    api_template = _generate_api(model_class, scaler_class, feature_names, model_name)
    api_path = str(out / "app.py")
    with open(api_path, "w") as f:
        f.write(api_template)
    files_created.append(api_path)

    # --- Generate requirements.txt ---
    req_template = "ferroml>=1.0.0\nnumpy>=1.24.0\nfastapi>=0.100.0\nuvicorn>=0.23.0\n"
    req_path = str(out / "requirements.txt")
    with open(req_path, "w") as f:
        f.write(req_template)
    files_created.append(req_path)

    # --- Generate Dockerfile ---
    dockerfile_template = _generate_dockerfile(model_name)
    docker_path = str(out / "Dockerfile")
    with open(docker_path, "w") as f:
        f.write(dockerfile_template)
    files_created.append(docker_path)

    return {
        "files_created": files_created,
        "model_size_bytes": model_sizes,
        "inference_template": inference_path,
        "api_template": api_path,
        "model_name": model_name,
        "n_features": len(feature_names),
    }


